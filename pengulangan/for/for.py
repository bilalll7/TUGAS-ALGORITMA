# For menggunakan struktur data list
ibukota = ['Bandung','Jakarta','Garut','Tasikmalaya']
ibukota[0] = 'Sumedang'
for kota in ibukota:
  print(kota)
else:
  print('-' * 20)

# For menggunakan struktur data tuple
# struktur data tuple tidak dapat diubah(readonly)
daftar_nilai = (90,20,75,20,15)
for nilai in daftar_nilai:
  print(nilai)
else:
  print('-' * 20)

# For menggunakan string
nama = 'Asep'
for huruf in nama:
  print(huruf)
else:
  print('-' * 20)